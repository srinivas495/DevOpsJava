
public class Addition {

	public static void main(String[] args) {
		
		int a = 30 , b = 20 , c ; 
		
		c = a + b;
		System.out.println("Result :"+ c);
		System.out.println("Addition a + b Successfully");
		
		c = a - b;
		System.out.println("Result :"+ c);
		System.out.println("Subtraction a - b Successfully");

		c = a * b;
		System.out.println("Result :"+ c);
		System.out.println("Multiplication a * b Successfully");
		
		c = a / b;
		System.out.println("Result :"+ c);
		System.out.println("Division a / b Successfully");
		
		
		int num1 = 1;
	    int num2 = 1;
	    int num3 = 2;
	    int num4 = 2;

	    --num1;
	    --num2;
	    num3++;
	    num4++;
	    
	    System.out.println("num1 = " + num1);
	    System.out.println("num2 = " + num2);
	    System.out.println("num3 = " + num3);
	    System.out.println("num4 = " + num4);
		
		
	}

}
